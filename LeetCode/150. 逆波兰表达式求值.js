/**
 * @param {string[]} tokens
 * @return {number}
 */
var evalRPN = function (tokens) {
  const stack = [];
  const map = { '+': 1, '-': 2, '*': 3, '/': 4, };
  for (let token of tokens) {
    if (map[token]) {
      let num = 0;
      let num1 = stack.pop();
      let num2 = stack.pop();
      switch (map[token]) {
        case 1:
          num = num1 + num2;
          break;
        case 2:
          num = num2 - num1;
          break;
        case 3:
          num = num2 * num1;
          break;
        case 4:
          num = num2 / num1;
          if (num > 0) {
            num = Math.floor(num)
          } else if (num < 0) {
            num = -Math.floor(Math.abs(num))
          } else {
            num = 0;
          }
          break;
      }
      stack.push(num);
    } else {
      stack.push(+token);
    }
  }
  return stack.pop();
};

